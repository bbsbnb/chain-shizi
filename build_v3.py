def build():
    result = {}
    
    # 收集所有字符
    all_chars = set()
    for c in COMMON_2500:
        if c.strip(): all_chars.add(c)
    for c in SUB_COMMON_1000:
        if c.strip(): all_chars.add(c)
    
    for g, chars in pep_data.items():
        for c in chars:
            if c.strip(): all_chars.add(c)
    
    # 逐个处理
    for char in all_chars:
        if char in EXISTING:
            result[char] = list(EXISTING[char])  # copy
        else:
            pinyin = py(char)
            strokes = sc(char)
            grade = PEP_GRADE.get(char, grade_by_sc(strokes))
            reading = make_reading(char, grade)
            radical = rad(char)
            meaning = f"{'一二三四五六'[grade-1]}年级常用字"
            result[char] = [char, pinyin, meaning, str(grade), reading, radical, strokes, '']
    
    # 按年级排序
    by_grade = {g: [] for g in range(1, 7)}
    for char, entry in result.items():
        g = int(entry[3])
        if 1 <= g <= 6:
            by_grade[g].append(entry)
    
    output = []
    for g in range(1, 7):
        output.extend(by_grade[g])
    
    return output

def fmt(e):
    c, py_val, m, g, rd, rad_val, s, so = e
    rd = rd.replace('\\','\\\\').replace("'","\\'")
    m = m.replace('\\','\\\\').replace("'","\\'")
    so = so.replace('\\','\\\\').replace("'","\\'") if so else ''
    return f'["{c}","{py_val}","{m}","{g}","{rd}","{rad_val}",{s},"{so}"]'

