    # 匹配 ["字","拼音","释义","年级","阅读"]
    m = re.match(r'^(\s*)\["([^"]*)","([^"]*)","([^"]*)","([^"]*)","([^"]*)"\](,?)\s*$', line)
    if m:
        indent, char, pinyin, meaning, grade, reading, comma = m.groups()
        if char in RADICAL_STROKE:
            radical, strokes = RADICAL_STROKE[char].split(',')
            new_lines.append(f'{indent}["{char}","{pinyin}","{meaning}","{grade}","{reading}","{radical}",{strokes}]{comma}')
            updated += 1
        else:
            # 未匹配到的，尝试根据Unicode部首推断
            new_lines.append(f'{indent}["{char}","{pinyin}","{meaning}","{grade}","{reading}","?","?"]{comma}')
            skipped += 1
    else:
        new_lines.append(line)

