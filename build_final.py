def parse_existing():
    text = open(r'C:\Users\Administrator\WorkBuddy\2026-06-02-task-5\识字训练应用.html', 'r', encoding='utf-8').read()
    match = re.search(r'const CHAR_DB_RAW = \[(.*?)\];', text, re.DOTALL)
    if not match:
        return {}
    raw = match.group(1)
    existing = {}
    entry_pattern = re.compile(r'\["([^"]*)","([^"]*)","([^"]*)","([\d]+)","([^"]*)","([^"]*)",(\d+),"([^"]*)"\]')
    for m in entry_pattern.finditer(raw):
        char = m.group(1)
        entry = [m.group(1), m.group(2), m.group(3), m.group(4), m.group(5),
                 m.group(6), int(m.group(7)), m.group(8)]
        if char not in existing:
            existing[char] = entry
    print(f"Loaded {len(existing)} existing quality entries")
    return existing

