def get_pinyin(char):
    py = PINYIN_DICT.get(char, '')
    if py and py != 'None':
        return py
    try:
        from pypinyin import pinyin, Style
        r = pinyin(char, style=Style.TONE)
        if r and r[0]: return r[0][0]
    except:
        pass
    return ''

def get_radical(char):
    rad = RADICAL_DICT.get(char, '')
    if rad and rad != 'None':
        return rad
    return '?'

